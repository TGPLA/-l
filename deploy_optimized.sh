#!/bin/bash

# 优化的部署脚本
# 使用此脚本可以一次性完成部署，避免多次SSH连接

set -e

echo "========================================="
echo "开始部署阅读回响系统..."
echo "========================================="
echo ""

# ==========================================
# 第一步：检查环境
# ==========================================
echo "[1/5] 检查环境..."
cd /root/backend || { echo "错误：找不到backend目录"; exit 1; }
echo "✓ 后端目录存在"

# ==========================================
# 第二步：拉取最新代码（如果是Git仓库）
# ==========================================
echo ""
echo "[2/5] 更新代码..."
if [ -d ".git" ]; then
    echo "检测到Git仓库，拉取最新代码..."
    git pull || echo "⚠️  Git pull失败，继续"
else
    echo "⚠️  不是Git仓库，跳过代码拉取"
fi
echo "✓ 代码检查完成"

# ==========================================
# 第三步：重新构建并启动后端
# ==========================================
echo ""
echo "[3/5] 重新构建后端..."
docker-compose down
docker-compose up -d --build
echo "✓ 后端容器重启完成"

# ==========================================
# 第四步：检查容器状态
# ==========================================
echo ""
echo "[4/5] 检查容器状态..."
sleep 3
docker-compose ps
echo "✓ 容器状态检查完成"

# ==========================================
# 第五步：显示部署完成信息
# ==========================================
echo ""
echo "========================================="
echo "✅ 部署成功！"
echo "========================================="
echo ""
echo "后端访问地址：http://localhost:8080"
echo "查看日志：cd /root/backend && docker-compose logs -f"
echo ""
echo "下一步：请在1Panel中上传前端文件"
echo "  - dist/index.html -> /opt/1panel/www/sites/readrecall/index/"
echo "  - dist/assets/*  -> /opt/1panel/www/sites/readrecall/index/assets/"
echo ""

